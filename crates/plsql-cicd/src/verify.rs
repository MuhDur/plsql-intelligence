    #[test]
    fn in_place_mismatch_keeps_verify_options_safe() {
        // End-to-end seam check: a failed confirmation means the caller never
        // sets allow_in_place, so VerifyOptions still refuses an in-place run.
        let mut out = Vec::new();
        let confirmed = confirm_in_place_verification(true, "HR_PROD", once("nope\n"), &mut out)
            .map(|d| d == InPlaceDecision::Confirmed)
            .unwrap_or(false);
        let opts = VerifyOptions {
            allow_in_place: confirmed,
            schema_override: Some("HR_PROD".to_string()),
            ..VerifyOptions::default()
        };
        assert!(
            matches!(
                opts.validated_schema(),
                Err(VerifyError::InPlaceVerificationRefused { .. })
            ),
            "unconfirmed in-place must stay refused by VerifyOptions"
        );
    }
